package exam19june;

public class question4 {
    public static void main(String[] args) {
        int a=5;

        System.out.printf("original:%d",a).println();
        System.out.printf("Pre-increment:%d",++a).println();
        System.out.printf("Post-increment:%d",a++).println();
        System.out.printf("Pre-decrement:%d",a--).println();
        System.out.printf("Post-decrement:%d",--a).println();


    }
}
