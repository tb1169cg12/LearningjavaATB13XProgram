package class_again;

public class increment_decrement {
    public static void main(String[] args) {
        int a =10;
        int b =a++;
        System.out.println(a);
        System.out.println(b);
        int c=10;
        int d=++c;
        System.out.println(c);
        System.out.println(d);
        int e=10;
        System.out.println(++e);
        System.out.println(e);
    }
}
