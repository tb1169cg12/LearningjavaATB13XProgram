package ex_07_17JUNE;

public class Lab_010_loop {
    public static void main(String[] args) {
        for(int i=0;i<10;i++){//start from 0 to 9 total 10
            System.out.println(i);
        }
        for(int j=1;j<=10;j++){//start from 1 to 10 total 10
            System.out.println(j);}
    }
}
