package ex_07_17JUNE;

public class Lab_007_int_float_long_loop {
    public static void main(String[] args) {
        for(int i=0;i<10;i++){
            System.out.println(i);

        }
        for(float a=0.0f;a<10;a++){
            System.out.println(a);
        }
        for(long l=1l;l<10;l++){
            System.out.println(l);
        }
    }
}
